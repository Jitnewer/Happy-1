<script>
export default {
  name: 'NavBarLoggedInAdminAndSuperUser.vue',
  emits: ['logout'],
  inject: ['sessionSBService'],

  data () {
    return {
      user: null,
      showDropdown: false,
      showProfile: false,
      smallVersionDasboard: false,
      showDashboard: true,
      randomColor: ''
    }
  },
  async created () {
    try {
      if (window.innerWidth <= 860) {
        this.showDashboard = false
      }
      const userAndToken = await this.sessionSBService.asyncFindByEmail(JSON.parse(localStorage.getItem('userDetails')).mail)
      this.user = userAndToken.body
      if (this.user.profilePic === '') {
        this.user.profilePic = null
      }
    } catch (e) {
      console.error(e)
    }
    const fullname = `${this.user.firstname} ${this.user.lastname}`
    let initials = ''
    const words = fullname.split(' ')
    if (words.length === 1) {
      initials = fullname.charAt(0).toUpperCase()
    } else {
      initials = words[0].charAt(0).toUpperCase() + words[1].charAt(0).toUpperCase()
    }
    this.randomColor = this.getRandomColor()

    this.drawProfilePicture(initials, this.randomColor)
    this.drawProfilePictureDashboard(initials, this.randomColor)
    this.updateShowNav()
  },
  methods: {
    handleLogout () {
      this.sessionSBService.signOut()
      this.$store.commit('setLoggedInAsAdmin', false)
      this.$store.commit('setLoggedInAsSuperUser', false)

      this.$router.push({ path: '/home' })
    },
    toggleDropdown (event) {
      event.stopPropagation()
      this.showDropdown = !this.showDropdown
    },
    toggleProfile (event) {
      event.stopPropagation()
      this.showProfile = !this.showProfile
    },
    toggleDasboard (event) {
      event.stopPropagation()
      if (window.innerWidth > 860) {
        this.showDashboard = true
        this.smallVersionDasboard = !this.smallVersionDasboard
      } else if (window.innerWidth <= 860) {
        this.smallVersionDasboard = !this.smallVersionDasboard
        this.showDashboard = false
      }
    },
    checkDashboard () {
      if (window.innerWidth > 860) {
        this.showDashboard = true
        this.smallVersionDasboard = false
      } else if (window.innerWidth <= 860) {
        this.showDashboard = false
        this.smallVersionDasboard = false
      }
    },
    updateShowNav () {
      this.showNav = window.innerWidth >= 860
    },
    showDropdownContent () {
      this.$refs.dropdownContent.style.visibility = 'visible'
    },
    drawProfilePicture (initials, bgColor) {
      this.$nextTick(() => {
        const canvas = this.$refs.profileCanvas
        if (canvas != null) {
          const ctx = canvas.getContext('2d')
          const fontSize = 15
          const fontFamily = 'Poppins, sans-serif'

          ctx.fillStyle = bgColor // Random background color
          ctx.fillRect(0, 0, canvas.width, canvas.height)

          ctx.fillStyle = '#ffffff' // Text color
          ctx.font = `${fontSize}px ${fontFamily}`
          ctx.textAlign = 'center'
          ctx.textBaseline = 'middle'

          const textY = canvas.height / 2

          ctx.fillText(initials, canvas.width / 2, textY)
        }
      })
    },
    drawProfilePictureDashboard (initials, bgColor) {
      this.$nextTick(() => {
        const canvas = this.$refs.profileCanvasDashboard
        if (canvas != null) {
          const ctx = canvas.getContext('2d')
          const fontSize = 15
          const fontFamily = 'Poppins, sans-serif'

          ctx.fillStyle = bgColor // Random background color
          ctx.fillRect(0, 0, canvas.width, canvas.height)

          ctx.fillStyle = '#ffffff' // Text color
          ctx.font = `${fontSize}px ${fontFamily}`
          ctx.textAlign = 'center'
          ctx.textBaseline = 'middle'

          const textY = canvas.height / 2

          ctx.fillText(initials, canvas.width / 2, textY)
        }
      })
    },
    handleOutsideClick (event) {
      if (!event.target.matches('.dropdown')) {
        this.showDropdown = false
      }
      if (!event.target.matches('.dropdown-profile')) {
        this.showDropdown = false
      }
    },
    getRandomColor () {
      const letters = '0123456789ABCDEF'
      let color = '#'
      for (let i = 0; i < 6; i++) {
        color += letters[Math.floor(Math.random() * 16)]
      }
      return color
    }
  },
  mounted () {
    window.addEventListener('click', this.handleOutsideClick)
    window.addEventListener('resize', this.updateShowNav)
    window.addEventListener('resize', this.checkDashboard)
  },
  beforeUnmount () {
    window.removeEventListener('click', this.handleOutsideClick)
    window.removeEventListener('resize', this.updateShowNav)
    window.removeEventListener('resize', this.checkDashboard)
  }
}
